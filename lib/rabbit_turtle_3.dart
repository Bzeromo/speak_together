class RabbitRabbitTurtle3 extends StatefulWidget {
  @override
  State<StatefulWidget> createState() => new _RabbitRabbitTurtle3State();
}

class _RabbitRabbitTurtle3State extends State<RabbitRabbitTurtle3> {
  late Timer _timer;
  late String story;

  _RabbitRabbitTurtle3State() {
    _timer = Timer(const Duration(seconds: 0), () {
      setState(() {
        final player = AudioPlayer();
        player.play(AssetSource('sounds/rabbit_story1_3.wav'));
        story = "하지만 거북이는 토끼가 자는동안";
      });
    });
    _timer = Timer(const Duration(seconds: 4), () {
      setState(() {
        story = "열심히 기어갔어요.";
      });
    });
    _timer = Timer(const Duration(seconds: 6), () {
      setState(() {
        story = "조금씩 조금씩 가까워지더니";
      });
    });
    _timer = Timer(const Duration(seconds: 10), () {
      setState(() {
        story = '이번엔 토끼를 앞서가기 시작했어요.';
      });
    });
    _timer = Timer(const Duration(seconds: 12), () {
      setState(() {
        story = '"나는 토끼를 꼭 이길거야!"';
      });
    });
    _timer = Timer(const Duration(seconds: 15), () {
      setState(() {
        story = '라고 생각하면서 말이죠.';
      });
    });
    _timer = Timer(const Duration(seconds: 19), () {
      setState(() {
        Navigator.push(context,
            MaterialPageRoute(builder: (context) => RabbitRabbitTurtle1_3()));
      });
    });

  }

  @override
  void dispose() {
    super.dispose();
    _timer.cancel();
  }

  @override
  Widget build(BuildContext context) {
    SystemChrome.setPreferredOrientations(
        [DeviceOrientation.landscapeLeft, DeviceOrientation.landscapeRight]);

    return MaterialApp(
        home: Scaffold(
            body: SizedBox(
                width: double.infinity,
                height: double.infinity,
                child: Container(
                  decoration: BoxDecoration(
                    image: DecorationImage(
                      fit: BoxFit.cover,
                      image: AssetImage('assets/images/토거3.png'),
                    ),
                  ),
                  child: Container(
                    margin: EdgeInsets.only(top: 10, bottom: 320),
                    child: Center(
                      child: Text(story, style: TextStyle(color: Color(0xff692d32), fontSize: 25, fontWeight: FontWeight.w700)),
                    ),
                  ),
                )
            )
        )
    );
  }
}
